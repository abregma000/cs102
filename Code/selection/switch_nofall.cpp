#include <iostream>

using namespace std;

int main()
{
   char c;
   cout<<"Enter choice: "<<endl;
   cin>>c;
   switch(c)
     {
      case 'A':
	cout<<"A"<<endl;
	break;
      case 'B':
	cout<<"B"<<endl;
	break;
      case 'C':
	cout<<"C"<<endl;
	break;
      default:
	cout<<"Invalid"<<endl;
     }   
   return 0;
}

   
	
